/**
 * A JSON-serializable value. Used for schema-less event content and room
 * metadata payloads.
 */
export type JsonValue =
	| string
	| number
	| boolean
	| null
	| JsonValue[]
	| { [key: string]: JsonValue };

/** A JSON object payload. */
export type JsonObject = { [key: string]: JsonValue };

/** Broad media category used by {@link AttachmentReference}. */
export type AttachmentKind = "image" | "video" | "audio" | "file";

/**
 * Host-owned attachment metadata that can be embedded in event content.
 *
 * `id` is an opaque identifier. ChatCore stores and synchronizes the reference,
 * but deliberately does not upload, download, authorize, or delete the
 * underlying file. The host application resolves the identifier and owns the
 * complete attachment lifecycle.
 */
export type AttachmentReference = JsonObject & {
	/** Opaque attachment identifier issued by the host application. */
	id: string;
	/** Broad media category used by clients for presentation. */
	kind: AttachmentKind;
	/** Original filename, when the host chooses to expose it. */
	name?: string;
	/** Declared or detected MIME type. */
	mimeType?: string;
	/** File size in bytes. */
	size?: number;
	/** Pixel width for visual media. */
	width?: number;
	/** Pixel height for visual media. */
	height?: number;
	/** Playback duration in milliseconds for audio or video. */
	durationMs?: number;
};

/**
 * The atomic, immutable unit of state in ChatCore. Once written, a
 * `FlowEvent` is never updated or deleted — the historical state of a room is
 * computed by replaying its events.
 */
export interface FlowEvent {
	/** Unique identifier generated by the storage backend. */
	id: string;
	/** Identifier of the isolated conversation boundary this event belongs to. */
	roomId: string;
	/** The entity that initiated the event. */
	senderId: string;
	/** Dot-notated namespace identifier, e.g. `message.text`, `room.state.name`. */
	type: string;
	/**
	 * When present (including the empty string), marks this as a "state event"
	 * representing durable room configuration rather than transient timeline
	 * traffic.
	 */
	stateKey: string | null;
	/** Schema-less JSON payload carrying event-specific data. */
	content: JsonObject;
	/** Creation time, epoch milliseconds. */
	timestamp: number;
	/**
	 * Strictly-increasing, globally unique sequential integer assigned at
	 * publish time. Drives real-time synchronization.
	 */
	sequenceId: number;
}

/** An isolated chat boundary. */
export interface Room {
	id: string;
	creatorId: string;
	/** Creation time, epoch milliseconds. */
	createdAt: number;
	metadata: JsonObject;
}

/** A parent → child link between two events (threading / DAG). */
export interface EventEdge {
	eventId: string;
	parentEventId: string;
}

/** Input for {@link ChatCore.createRoom}. */
export interface CreateRoomInput {
	creatorId: string;
	metadata?: JsonObject;
}

/** Options for {@link ChatCore.listRooms}. */
export interface ListRoomsOptions {
	/**
	 * Maximum number of rooms to return.
	 *
	 * @default ChatCoreOptions.defaultLimit
	 */
	limit?: number;
	/**
	 * Sort direction by room creation time.
	 *
	 * @default "asc"
	 */
	order?: "asc" | "desc";
}

/** Input for {@link ChatCore.publishEvent}. */
export interface PublishEventInput {
	roomId: string;
	senderId: string;
	type: string;
	/**
	 * Marks the event as a state event and upserts the room-state projection
	 * under the composite key `[roomId, type, stateKey]`. The empty string is a
	 * valid state key.
	 */
	stateKey?: string;
	content?: JsonObject;
	/** Parent event ids, persisted as edges for threading / replies. */
	parentEventIds?: string[];
}

/** Result of {@link ChatCore.publishEvent}. */
export interface PublishEventResult {
	event: FlowEvent;
	sequenceId: number;
}

/** Options for {@link ChatCore.getRoomTimeline}. */
export interface GetTimelineOptions {
	limit?: number;
	/** Return only events strictly before this sequence id (for pagination). */
	beforeSequenceId?: number;
}

/** Options for {@link ChatCore.getSyncStream}. */
export interface GetSyncStreamOptions {
	/** Return only events with `sequenceId` strictly greater than this token. */
	sinceSequenceId?: number;
	limit?: number;
	/**
	 * Restrict the stream to these rooms. The host resolves which rooms the
	 * caller may observe from its own auth and passes them here.
	 *
	 * - Omitted → full global stream (back-compat for single-tenant embeds).
	 * - `[]` → returns no events.
	 * - `[...]` → returned events are filtered with `roomId IN (...)`.
	 *
	 * For scoped streams, ChatCore also reads a global page boundary to advance
	 * `nextToken` across out-of-scope sequence gaps. This means out-of-scope
	 * events are not returned, but their sequence positions can affect the resume
	 * token.
	 *
	 * **Scale note (v0.2):** `roomId IN (...)` is acceptable for moderate room
	 * sets. High-fan-out actors (thousands of rooms) may benefit from a
	 * per-room cursor approach, tracked as a follow-up.
	 */
	roomIds?: string[];
}

/** Result of {@link ChatCore.getSyncStream}. */
export interface SyncStreamResult {
	events: FlowEvent[];
	/**
	 * Resume token for the next call. For global streams this is the last
	 * returned event sequence id. For scoped streams this may also advance to the
	 * global page boundary so clients do not repeatedly scan out-of-scope gaps.
	 */
	nextToken: number;
}
